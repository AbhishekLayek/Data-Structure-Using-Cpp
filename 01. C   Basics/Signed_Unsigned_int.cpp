/* Unsigned int: It Will Store Only Positive (+ve) Integers.
   Signed int: It Will Store Both Positive (+ve) & Negative (-ve) Integers. 
*/ 
#include<iostream>
using namespace std;
int main(){
    // Signed int
    int i = -5;
    cout<< i <<endl;

    // Unsigned int
    unsigned int u = -5;
    cout<< u <<endl;
}
